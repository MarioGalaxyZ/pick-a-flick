import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { spawnSync } from 'child_process';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, '..');
const CLIP_REL = 'audio/ui/coin-flip/coin-flip-1.mp3';
const CLIP_PATH = path.join(projectRoot, CLIP_REL);
const OUT_PATH = path.join(projectRoot, 'coin-flip-blip-map.js');
const LOCAL_FFMPEG = path.join(projectRoot, 'node_modules', 'ffmpeg-static', 'ffmpeg.exe');

async function resolveFfmpeg() {
    try {
        const mod = await import('ffmpeg-static');
        if (mod.default && fs.existsSync(mod.default)) return mod.default;
    } catch {
        // optional
    }
    if (fs.existsSync(LOCAL_FFMPEG)) return LOCAL_FFMPEG;
    return 'ffmpeg';
}

function decodeToMonoPcm(ffmpegPath, inputPath) {
    const result = spawnSync(
        ffmpegPath,
        ['-i', inputPath, '-f', 's16le', '-ac', '1', '-ar', '44100', 'pipe:1'],
        { encoding: 'buffer', maxBuffer: 64 * 1024 * 1024 }
    );
    if (result.status !== 0 || !result.stdout?.length) {
        const err = result.stderr?.toString('utf8')?.slice(-400) || 'unknown ffmpeg error';
        throw new Error(`Failed to decode ${inputPath}: ${err}`);
    }
    return { samples: result.stdout, sampleRate: 44100 };
}

function getClipDurationMs(ffmpegPath, inputPath) {
    const result = spawnSync(
        ffmpegPath,
        ['-i', inputPath, '-f', 'null', '-'],
        { encoding: 'utf8' }
    );
    const match = (result.stderr || '').match(/Duration:\s(\d+):(\d+):(\d+(?:\.\d+)?)/);
    if (!match) return null;
    const hours = parseInt(match[1], 10);
    const mins = parseInt(match[2], 10);
    const secs = parseFloat(match[3]);
    return Math.round((hours * 3600 + mins * 60 + secs) * 1000);
}

function computeEnvelope(samples, sampleRate, windowMs = 4) {
    const windowSamples = Math.max(1, Math.round((windowMs / 1000) * sampleRate));
    const count = Math.floor(samples.length / 2);
    const envelope = [];

    for (let i = 0; i < count; i += windowSamples) {
        let peak = 0;
        const end = Math.min(i + windowSamples, count);
        for (let j = i; j < end; j += 1) {
            const sample = samples.readInt16LE(j * 2);
            const abs = Math.abs(sample);
            if (abs > peak) peak = abs;
        }
        envelope.push({
            timeMs: (i / sampleRate) * 1000,
            amp: peak
        });
    }

    return envelope;
}

function findLocalMaxima(envelope, minGapMs = 45) {
    const peaks = [];
    for (let i = 1; i < envelope.length - 1; i += 1) {
        const prev = envelope[i - 1].amp;
        const cur = envelope[i].amp;
        const next = envelope[i + 1].amp;
        if (cur >= prev && cur > next && cur > 0) {
            peaks.push({ timeMs: envelope[i].timeMs, amp: cur });
        }
    }

    peaks.sort((a, b) => b.amp - a.amp);
    const kept = [];
    for (const peak of peaks) {
        if (kept.every((k) => Math.abs(k.timeMs - peak.timeMs) >= minGapMs)) {
            kept.push(peak);
        }
    }
    kept.sort((a, b) => a.timeMs - b.timeMs);
    return kept;
}

function pickFlipBlips(allPeaks, clipDurationMs) {
    const inWindow = allPeaks.filter((p) => p.timeMs >= 20 && p.timeMs <= clipDurationMs - 15);
    if (inWindow.length < 4) {
        throw new Error(`Expected at least 4 peaks, found ${inWindow.length}`);
    }

    const sortedByAmp = [...inWindow].sort((a, b) => b.amp - a.amp);
    const fifthAmp = sortedByAmp[4]?.amp ?? 0;
    const fourthAmp = sortedByAmp[3].amp;
    const ampCutoff = fifthAmp > 0 ? (fourthAmp + fifthAmp) / 2 : fourthAmp * 0.55;

    const strong = inWindow
        .filter((p) => p.amp >= ampCutoff)
        .sort((a, b) => a.timeMs - b.timeMs);

    if (strong.length >= 4) {
        return strong.slice(0, 4).map((p) => Math.round(p.timeMs));
    }

    return inWindow.slice(0, 4).map((p) => Math.round(p.timeMs));
}

function writeBlipMap({ blipTimesMs, clipDurationMs }) {
    const body = `// Auto-generated by scripts/analyze-coin-flip-blips.mjs — do not edit by hand.
window.coinFlipBlipMap = {
  clipPath: ${JSON.stringify(CLIP_REL)},
  blipTimesMs: ${JSON.stringify(blipTimesMs)},
  clipDurationMs: ${clipDurationMs}
};
`;
    fs.writeFileSync(OUT_PATH, body, 'utf8');
    console.log(`Wrote ${path.relative(projectRoot, OUT_PATH)}`);
    console.log('blipTimesMs:', blipTimesMs);
    console.log('clipDurationMs:', clipDurationMs);
}

async function main() {
    if (!fs.existsSync(CLIP_PATH)) {
        console.error(`Missing clip: ${CLIP_PATH}`);
        process.exit(1);
    }

    const ffmpegPath = await resolveFfmpeg();
    const clipDurationMs = getClipDurationMs(ffmpegPath, CLIP_PATH);
    const { samples, sampleRate } = decodeToMonoPcm(ffmpegPath, CLIP_PATH);
    const envelope = computeEnvelope(samples, sampleRate);
    const peaks = findLocalMaxima(envelope);

    let blipTimesMs;
    if (clipDurationMs && peaks.length >= 4) {
        blipTimesMs = pickFlipBlips(peaks, clipDurationMs);
    } else if (clipDurationMs) {
        blipTimesMs = [1, 2, 3, 4].map((i) => Math.round((clipDurationMs * i) / 5));
    } else {
        const totalMs = Math.round((samples.length / 2 / sampleRate) * 1000);
        blipTimesMs = [1, 2, 3, 4].map((i) => Math.round((totalMs * i) / 5));
    }

    const durationMs = clipDurationMs
        ?? Math.round((samples.length / 2 / sampleRate) * 1000);

    writeBlipMap({ blipTimesMs, clipDurationMs: durationMs });
}

main().catch((err) => {
    console.error(err.message || err);
    process.exit(1);
});
