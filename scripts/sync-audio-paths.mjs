import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { UI_AUDIO } from './audio-paths.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, '..');
const generatedDir = path.join(projectRoot, 'app', 'generated');

const output = `// Auto-generated by scripts/sync-audio-paths.mjs — do not edit by hand.
window.uiAudioPaths = ${JSON.stringify(UI_AUDIO, null, 2)};
`;

fs.writeFileSync(path.join(generatedDir, 'audio-paths.js'), output, 'utf8');
console.log('Wrote audio-paths.js');
