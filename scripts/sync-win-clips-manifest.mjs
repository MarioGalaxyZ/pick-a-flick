import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { WIN_CLIP_CATEGORIES } from './win-clip-categories.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, '..');
const AUDIO_EXTENSIONS = new Set(['.mp3', '.wav']);

function scanCategoryFolder(folderPath) {
    if (!fs.existsSync(folderPath)) {
        return null;
    }

    return fs.readdirSync(folderPath, { withFileTypes: true })
        .filter((entry) => {
            if (!entry.isFile()) return false;
            if (entry.name.startsWith('_')) return false;
            return AUDIO_EXTENSIONS.has(path.extname(entry.name).toLowerCase());
        })
        .map((entry) => entry.name)
        .sort((a, b) => a.localeCompare(b));
}

function formatManifestEntry(category, folder, files) {
    const folderWithSlash = `${folder.replace(/\\/g, '/')}/`;
    const fileLines = files.map((file) => `    ${JSON.stringify(file)}`).join(',\n');
    return `  ${JSON.stringify(category)}: {\n    folder: ${JSON.stringify(folderWithSlash)},\n    files: [\n${fileLines}\n    ]\n  }`;
}

function main() {
    const manifest = {};
    const warnings = [];

    for (const [category, folder] of Object.entries(WIN_CLIP_CATEGORIES)) {
        const absoluteFolder = path.join(projectRoot, folder);
        const files = scanCategoryFolder(absoluteFolder);

        if (files === null) {
            warnings.push(`Missing folder: ${folder}`);
            manifest[category] = { folder: `${folder}/`, files: [] };
            continue;
        }

        if (files.length === 0) {
            warnings.push(`No audio clips in: ${folder}`);
        }

        manifest[category] = { folder: `${folder}/`, files };
    }

    const entries = Object.entries(manifest)
        .map(([category, { folder, files }]) => formatManifestEntry(category, folder.replace(/\/$/, ''), files));

    const output = `// Auto-generated by scripts/sync-win-clips-manifest.mjs — do not edit by hand.
// Re-run npm run refresh-audio after adding clips to win_sounds/<category>/.
window.categoryWinClips = {
${entries.join(',\n')}
};
`;

    const outputPath = path.join(projectRoot, 'win-clips-manifest.js');
    fs.writeFileSync(outputPath, output, 'utf8');

    const totalClips = Object.values(manifest).reduce((sum, entry) => sum + entry.files.length, 0);
    console.log(`Wrote ${outputPath} (${Object.keys(manifest).length} categories, ${totalClips} clips).`);

    if (warnings.length > 0) {
        console.log('\nWarnings:');
        warnings.forEach((warning) => console.log(`  - ${warning}`));
    }
}

main();
